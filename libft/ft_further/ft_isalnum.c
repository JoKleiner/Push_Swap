#include "../libft.h"

int	ft_isalnum(int cha)
{
	if ((cha >= 'A' && cha <= 'Z')
		|| (cha >= 'a' && cha <= 'z')
		|| (cha >= '0' && cha <= '9'))
		return (1);
	return (0);
}
